package javaPackage;

public class Variable1 {
	


	public static void main(String[] args) {
		int a = 5;
		System.out.println(a);// int type local variable
		
		int x = 100;
		System.out.println(x);//int type local variable
		
		x  = 200;
		System.out.println(x);//int type local variable
		
		char c = 'a';
		System.out.println(c);// char type local variable, should be placed inside single quote
		
		boolean b=true;
		b=false;
		System.out.println(b);//false
		System.out.println(3>12);//false
		int e=100;
		int f=200;
		
		boolean z=e>f;
		System.out.println(z);//false
		
	}

}
