package javaPackage;

public class IfElse3 {

	public static void main(String[] args) {
		
		int q = 400;
		int w = 300;
		
		if (q < w)
		{
			 System.out.println("q is lesser");
		} else if (q == w)
		{
			 System.out.println("q is equal to w");
		}else
		{
			 System.out.println("q is greater");
		}
	}

}
