package javaPackage;

public class AssignmentOperator1 {

	public static void main(String[] args) {
		int a = 10;
		
		a += 3; // do not pass space b/w +=
		System.out.println(a);
		
		a -= 4; // do not pass space b/w -=
		System.out.println(a);
		
		a *= 2; // do not pass space b/w *=
		System.out.println(a);
		
		a /= 2; // do not pass space b/w /=
		System.out.println(a);
	}

}
